public class GitHubService
{
    private readonly IHttpClientFactory _httpClientFactory;

    public GitHubService(IHttpClientFactory httpClientFactory) =>
        _httpClientFactory = httpClientFactory;

    public async Task<ResultInfo?> GetAspNetCoreDocsBranchesAsync(string session)
    {
        //test.api.forclass.net/UserCenter/Account/GetUserFullName?session=
        var httpRequestMessage = new HttpRequestMessage(
            HttpMethod.Get,
            "http://test.api.forclass.net/UserCenter/Account/GetUserFullName?session=" + session)
        {
            Headers =
            {
                { "Accept", "application/vnd.github.v3+json" },
                { "User-Agent", "HttpRequestsConsoleSample" }
            }
        };

        var httpClient = _httpClientFactory.CreateClient();
        var httpResponseMessage = await httpClient.SendAsync(httpRequestMessage);

        httpResponseMessage.EnsureSuccessStatusCode();

        var contentStream =
                    await httpResponseMessage.Content.ReadAsStringAsync();

        var result = JsonConvert.DeserializeObject<ResultInfo>(contentStream);
        return result;
    }
}

